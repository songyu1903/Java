package b_oper;

public class Ex02 {

	public static void main(String[] args) {
		// 논리 연산자
		System.out.println(10 & 11);
		System.out.println(10 | 11);
		System.out.println(10 ^ 11);
		
		// NOT연산자
		System.out.println(~10);
		
		// 쉬프트 연산자
		System.out.println(10 >> 1);
		System.out.println(10 >> 2);
		System.out.println(10 << 1);
		System.out.println(10 << 2);
		
		System.out.println(0.1 + 0.2);
	}

}
