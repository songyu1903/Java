package a_variable;

public class Ex01 {
	public static void main(String[] args) {
		int age;
		long finger;
		float weight;
		double height;
		char sung;
		String name;
		
		age = 26;
		finger = 10L;
		weight = 48.8f;
		height = 171.2;
		sung = '김';
		name = "송호";		
		
		System.out.println(age);
		System.out.println(finger);
		System.out.println(weight);
		System.out.println(height);
		System.out.println(sung);
		System.out.println(name);
	}

}
